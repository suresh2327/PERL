#arthematic operators
#!usr/bin/perl
use strict;
use warnings;

my $a=10;
my $b=20;

#arthematic operators are used to perform mathematical operations on numbers. The following are the arthematic operators in perl:
print "-------Normal print statement-------\n";
print "Addition: ", $a + $b, "\n"; # addition operator
print "Subtraction: ", $a - $b, "\n"; # subtraction operator
print "Multiplication: ", $a * $b, "\n"; # multiplication operator
print "Division: ", $a / $b, "\n"; # division operator
print "Modulus: ", $a % $b, "\n"; # modulus operator

print "-------Using concatenation operator-------\n";
#we can write this print statement using the concatenation operator to make it more readable
print "Addition: " . ($a + $b) . "\n"; # addition operator
print "subtraction: " . ($a - $b) . "\n"; # subtraction operator
print "Multiplication: " . ($a * $b) . "\n"; # multiplication operator
print "Division: " . ($a / $b) . "\n"; # division operator
print "Modulus: " . ($a % $b) . "\n"; # modulus operator







